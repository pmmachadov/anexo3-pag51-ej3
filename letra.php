Crea una función que reciba como parámetro un valor numérico y devuelva una
cadena con el dicho valor expresado con dos valores para la parte decimal como
mínimo a rellenar con ceros si es necesario. La cadena resultante debe además incluir
un carácter # al principio y final. Por ejemplo, el valor 153.4 debería mostrarse como:
“#153.40#”.
